name1 = 'Tanmay'  # Single Quoted String
name2 = "Tanmay"  # Double Quoted String
name3 = '''Tanmay'''  # Triple Quoted String

# String Slicing
# Here it will assign the value of index 3 of Name1 to character variable
character = name1[3]
print(character)  # Output: m

# Here it will slice the string from the index 0 all the way till 4 but will exclude 4
shortname = name1[0:4]
print(shortname)  # Output: Tanm
